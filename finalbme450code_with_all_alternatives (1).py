    species_wav_output_folder = os.path.join(output_wav_dir, bird_folder_name)
    os.makedirs(species_wav_output_folder, exist_ok=True)

    # Creating species spectrogram subfolder
    species_spectrogram_output_folder = os.path.join(output_spectrogram_dir, bird_folder_name)
    os.makedirs(species_spectrogram_output_folder, exist_ok=True)

    # actual conversion of mp3 audio to wav files
    wav_files = convert_to_wav(input_folder, species_wav_output_folder)

    # actual creation of spectrograms
    print(f"Generating spectrograms for WAV files in {species_wav_output_folder}...")
    for wav_file in wav_files:
        generate_spectrogram(wav_file, species_spectrogram_output_folder)
    print(f"Finished spectrogram generation for {bird_folder_name}.\n")


