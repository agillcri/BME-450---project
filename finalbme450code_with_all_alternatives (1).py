# initializing data and labels lists
data = []
labels = []

# using a loop to go through the three bird species and performing the following:
# making sure there is a folder containing the spectrograms and telling user if there is not
# making sure the actual spectrogram images exist and telling user if it does not
# then it contains another for loop where the spectrograms are set to specific dimensions defined previously
# and it converts them to greyscale. It also normalizes the data from 0 to 1.
# then the data is added to the lists
for i, species in enumerate(bird_species_folders):
    species_spectrogram_folder = os.path.join(output_spectrogram_dir, species)

    if not os.path.exists(species_spectrogram_folder):
        print(f"Warning: Species spectrogram folder does not exist: {species_spectrogram_folder}. Skipping.")
        continue

    print(f"Loading spectrograms for {species} from {species_spectrogram_folder}...")
    image_files = glob.glob(os.path.join(species_spectrogram_folder, '*.png'))
    if not image_files:
        print(f"  No PNG files found in {species_spectrogram_folder}. Skipping.")
        continue

    for img_path in image_files:
        try:
            # Loading images as grayscale
            img = load_img(img_path, color_mode='grayscale', target_size=(IMG_HEIGHT, IMG_WIDTH))
            img_array = img_to_array(img)

            # Normalizing to [0, 1] range
            img_array = img_array / 255.0

            data.append(img_array)
            labels.append(species)
        except Exception as e:
            print(f"Error loading or processing {img_path}: {e}")
# list conversion into arrays
data = np.array(data)
labels = np.array(labels)

