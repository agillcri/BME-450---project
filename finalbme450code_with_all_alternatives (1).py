# this function is creating the noise reduction process that will be used and it makes
# sure that a folder is created
def apply_noise_reduction(wav_filepath, output_folder):
    """Applies noise reduction to a WAV file and saves it."""
    filename = os.path.basename(wav_filepath)
    name, ext = os.path.splitext(filename)
    output_filepath = os.path.join(output_folder, name + '_nr' + ext)

    if os.path.exists(output_filepath):
        # additionally, if there is already a noise-reduced wav file, it does not make a new one
        return output_filepath

# this is where the noise reduction library is used to perform the reduction
    try:
        y, sr = librosa.load(wav_filepath, sr=None)
        reduced_noise_audio = nr.reduce_noise(y=y, sr=sr, stationary=True)

        # this helps save the noise-reduced wav as an audio file and if there is an
        # error the user is informed
        sf.write(output_filepath, reduced_noise_audio, sr)
        print(f"  Applied noise reduction to {filename}")
        return output_filepath
    except Exception as e:
        print(f"  Error applying noise reduction to {filename}: {e}")
        return None

# this function applies the other function to actually create noise-reduced wavs and spectrograms
def process_bird_folder_with_noise_reduction(bird_folder_name):
    """Processes a single bird species folder: converts to WAV, applies noise reduction, and generates spectrograms."""
    input_folder = os.path.join(base_drive_path, bird_folder_name)

    # Creating a species subfolder for wavs
    species_wav_output_folder = os.path.join(output_wav_dir, bird_folder_name)
    os.makedirs(species_wav_output_folder, exist_ok=True)

    # converting to wav files
    print(f"Processing {bird_folder_name}:")
    converted_wav_files = convert_to_wav(input_folder, species_wav_output_folder)

    # Creating a species subfolder for noise-reduced wavs
    species_noise_reduced_output_folder = os.path.join(output_noise_reduced_wav_dir, bird_folder_name)
    os.makedirs(species_noise_reduced_output_folder, exist_ok=True)

    # actually applying the noise reduction for the wav files using previously defined functions
    print(f"Applying noise reduction for WAV files in {species_wav_output_folder}...")
    noise_reduced_files = []
    for wav_file in converted_wav_files:
        reduced_file = apply_noise_reduction(wav_file, species_noise_reduced_output_folder)
        if reduced_file:
            noise_reduced_files.append(reduced_file)
    print(f"Finished noise reduction for {bird_folder_name}. Total reduced: {len(noise_reduced_files)}\n")

    # Creating species subfolder for noise-reduced spectrograms
    species_spectrogram_output_folder = os.path.join(output_spectrogram_dir, bird_folder_name)
    os.makedirs(species_spectrogram_output_folder, exist_ok=True)

    # creating noise-reduced spectrograms
    print(f"Generating spectrograms for noise-reduced WAV files in {species_noise_reduced_output_folder}...")
    for nr_file in noise_reduced_files:
        generate_spectrogram(nr_file, species_spectrogram_output_folder)
    print(f"Finished spectrogram generation for {bird_folder_name}.\n")

